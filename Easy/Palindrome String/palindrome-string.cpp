class Solution{
public:	
	
	
	int isPalindrome(string s)
	{
	   int begin=0;
	   int res=1;
	   int last=s.length()-1;
	   while(last>begin){
	       if(s[last]!=s[begin]){
	           res=0;break;
	       }last--;
	       begin++;
	   }return res;
	}

};

